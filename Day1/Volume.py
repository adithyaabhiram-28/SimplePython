# Cube
side1 = float(input("Enter the side length of the cube: "))
volume1 = side1 ** 3
print("Volume of the cube is:", volume1)


# Cylinder
rad1 = float(input("Enter the radius of the cylinder: "))
height1 = float(input("Enter the height of the cylinder: "))
volume2 = 3.14 * rad1 * rad1 * height1
print("Volume of the cylinder is:", volume2)